namespace MauiKeyboardEffects;

public enum KeyboardButtonType
{
    /// <summary>
    /// Shows the text "Return" in the return key.
    /// </summary>
    Default,

    /// <summary>
    /// Shows the text "Search" in the return key.
    /// </summary>
    Search,

    /// <summary>
    /// Shows the text "Next" in the return key.
    /// </summary>
    Next,

    /// <summary>
    /// Shows the text "Save" in the return key.
    /// </summary>
    Save,

    /// <summary>
    /// Shows the text "Go" in the return key.
    /// </summary>
    Go,

    /// <summary>
    /// Shows the text "Join" in the return key.
    /// </summary>
    Join,

    /// <summary>
    /// Shows the text "Route" in the return key.
    /// </summary>
    Route,

    /// <summary>
    /// Shows the text "Send" in the return key.
    /// </summary>
    Send,

    /// <summary>
    /// Shows the text "Done" in the return key.
    /// </summary>
    Done,

    /// <summary>
    /// Used for deleting from the entry field.
    /// </summary>
    Delete,

    /// <summary>
    /// Used for dismissing the keyboard.
    /// </summary>
    Dismiss,

    /// <summary>
    /// Allows for Images and custom text to be set.
    /// </summary>
    Custom,

    /// <summary>
    /// Side button that offers optional functionality.
    /// </summary>
    OptionalButton,
}
